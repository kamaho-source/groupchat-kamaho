<?php

use Illuminate\Support\Str;

return [

    // …（その他の設定省略）…

    /*
    |--------------------------------------------------------------------------
    | Default Session Driver
    |--------------------------------------------------------------------------
    |
    | このオプションは、アプリケーションがセッションをどこに保存するか
    | を制御します。デフォルトは "cookie" ドライバです。
    |
    */
    'driver' => env('SESSION_DRIVER', 'cookie'),

    /*
    |--------------------------------------------------------------------------
    | Session Cookie Name
    |--------------------------------------------------------------------------
    |
    | ここで設定した名前でセッションクッキーが発行されます。
    |
    */
    'cookie' => env(
        'SESSION_COOKIE',
        Str::slug(env('APP_NAME', 'laravel'), '_').'_session'
    ),

    /*
    |--------------------------------------------------------------------------
    | Session Cookie Domain
    |--------------------------------------------------------------------------
    |
    | どのドメインに対してセッションクッキーを発行するかを指定します。
    | デフォルトは null（ドメイン属性なし＝ホストオンリー Cookie）。
    |
    */
    'domain' => env('SESSION_DOMAIN', null),

    /*
    |--------------------------------------------------------------------------
    | HTTPS Only Cookies
    |--------------------------------------------------------------------------
    |
    | true にすると HTTPS 接続時のみクッキーを送信します。開発環境では false。
    |
    */
    'secure' => env('SESSION_SECURE_COOKIE', false),

    /*
    |--------------------------------------------------------------------------
    | Same-Site Cookies
    |--------------------------------------------------------------------------
    |
    | クロスサイトリクエストで Cookie を送信する際の SameSite ポリシー。
    | デフォルトは 'lax' です。
    |
    */
    'same_site' => env('SESSION_SAME_SITE', 'lax'),

    /*
    |--------------------------------------------------------------------------
    | Partitioned Cookies
    |--------------------------------------------------------------------------
    |
    | ブラウザがサポートしていればパーティション付きクッキーを利用します。
    |
    */
    'partitioned' => env('SESSION_PARTITIONED_COOKIE', false),

    // …（他の設定が続く場合は適宜残してください）…

];
